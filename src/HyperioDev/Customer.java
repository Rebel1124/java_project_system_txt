package HyperioDev;

public class Customer {
	
	// Attributes
	String name;
	String telNumber;
	String email;
	String address;
	
	// Methods (Constructor)
	public Customer(String name, String telNumber, String email, String address) {
	  this.name = name;
	  this.telNumber = telNumber;
	  this.email = email;
	  this.address = address;
	}

}
